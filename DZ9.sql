DROP PROCEDURE IF EXISTS hello//
CREATE PROCEDURE hello()
BEGIN
  CASE 
    WHEN CURRENT_TIME >= CAST('6:00:00' as TIME) and  CURRENT_TIME < CAST('12:00:00' as TIME) THEN
      SELECT 'Доброе утро';
    WHEN CURRENT_TIME >= CAST('12:00:00' as TIME) and  CURRENT_TIME < CAST('18:00:00' as TIME) THEN
      SELECT 'Добрый день';
    WHEN CURRENT_TIME >= CAST('18:00:00' as TIME) and  CURRENT_TIME < CAST('23:59:59' as TIME) THEN
      SELECT 'Добрый вечер';
    ELSE
      SELECT 'Ошибка';
  END CASE;
END//
